# Logical Type
log1<-TRUE
log2<-FALSE

print("Logical: ")
print(log1)
print(log2)

# Integer Type
iv<-76L

print("Integer: ")
print(iv)

# Numeric type
nv1<-34.5
nv2<-928

print("Numeric: ")
print(nv1)
print(nv2)

# Complex Type
cv<-5+3i

print("Complex: ")
print(cv)

#C Character type
chv1<-"Name"
